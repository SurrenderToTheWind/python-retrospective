SIGNS = (
    (19, 'Козирог'),
    (18, 'Водолей'),
    (20, 'Риби'),
    (20, 'Овен'),
    (20, 'Телец'),
    (20, 'Близнаци'),
    (21, 'Рак'),
    (22, 'Лъв'),
    (22, 'Дева'),
    (22, 'Везни'),
    (21, 'Скорпион'),
    (21, 'Стрелец')
)


def what_is_my_sign(day, month):
    if (SIGNS[month-1][0] >= day):
        return SIGNS[month-1][1]
    else:
        return SIGNS[month % 12][1]
